#Faça um programa que solicite o salário de três servidoras e mostre a média de salário deles.

salario1 = float(input('Informe o primeiro salario:'))
salario2 = float(input('Informe o segundo salario:'))
salario3 = float(input('Informe o segundo salario'))

media = ((salario1 + salario2 + salario3)/3)

print("A media dos salarios e: ", media)
